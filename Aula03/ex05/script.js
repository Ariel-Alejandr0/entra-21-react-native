let v1 = parseFloat(window.prompt("Digite o primeiro valor: "))
let v2 = parseFloat(window.prompt("Digite o segundo valor: "))

window.document.write(`${v1} + ${v2} = ${v1 + v2} <br>`)
window.document.write(`${v1} - ${v2} = ${v1 - v2} <br>`)
window.document.write(`${v1} * ${v2} = ${v1 * v2} <br>`)
window.document.write(`${v1} + ${v2} = ${v1 / v2} <br>`)